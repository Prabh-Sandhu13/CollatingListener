package MessageProcessor;

public class Message {
	int seqNum;
	String text;
	
	public int getSeqNum() {
		return seqNum;
	}
	
	public void setSeqNum(int seqNum) {
		this.seqNum = seqNum;
	}
	
	public String getText() {
		return text;
	}
	
	public void setText(String text) {
		this.text = text;
	}
	
}
